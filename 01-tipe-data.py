# tipe data Boolean
print(True)

# tipe data String
print("Hello World")
print('Belajar phyton sangat menantang')

# tipe data Integer
print(20)

# tipe data float
print(3.14)

# tipe data Hexadecimal
print(0x9a)

# tipe data Complex
print(5j)

# tipe data List (List sama seperti array pada bahasa pemrograman umum lainnya)
print([1, 2, 3, 4, 5])
print(["satu", "dua", "tiga"])

# tipe data Tuple (Tuple sama dengan list hanya saja datanya tidak dapat di ubah / consntant)
print((5, 6, 7, 8, 9))
print(("lima", "enam", "tujuh"))

# tipe data Dictionary
print({"nama": "Habibi", "umur": 20})
biodata = {"nama": "Fath", "umur": 21}
print(biodata)  # cetak biodata
print(type(biodata))  # cek type data dalam biodata


